#atribuição simples, sinal de =
saldo = 10000 #estramos atribuindo o valor de 500 ao saldo
print(f'R$ {saldo}')

#atribuição com adição
saldo += 200
print(f'R$ {saldo}')

#atribuição com subtração
saldo -= 400
print(f'R$ {saldo}')


#atribuição com multiplicação
saldo *= 300
print(f'R$ {saldo}')

#atribuição com divisão e divisão inteira
saldo /=5.5
print(f'R${saldo}')

saldo //=5.5
print(f'R${saldo}')

saldo2 = 5000

#atribuição com módulo
saldo2 %= 900
print(f'R${saldo2}')

saldo2 **=2
print(f'R${saldo2}')


